const CRON_REGEX =
  /^(\*(\/[0-9]+)?|([0-9]|,|-|\/)+)\s+(\*(\/[0-9]+)?|([0-9]|,|-|\/)+)\s+(\*(\/[0-9]+)?|([0-9]|,|-|\/)+)\s+(\*(\/[0-9]+)?|([0-9]|,|-|\/)+)\s+(\*(\/[0-9]+)?|([0-9]|,|-|\/)+)\s+(\*(\/[0-9]+)?|([0-9]|,|-|\/)+)$/
/**
 * 验证是否为cron表达式（支持6位，包含秒）
 */
export function isCronExp(value: string) {
  return CRON_REGEX.test(value)
}
